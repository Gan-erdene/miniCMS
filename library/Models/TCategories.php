    /**
     * Set lft
     *
     * @param integer $lft
     * @return TCategories
     */
    public function setLft($lft)
    {
        $this->lft = $lft;

        return $this;
    }

    /**
     * Get lft
     *
     * @return integer 
     */
    public function getLft()
    {
        return $this->lft;
    }

    /**
     * Set rgt
     *
     * @param integer $rgt
     * @return TCategories
     */
    public function setRgt($rgt)
