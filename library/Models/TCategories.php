     *
     * @param float $pos
     * @return TCategories
     */
    public function setPos($pos)
    {
        $this->pos = $pos;

        return $this;
    }

    /**
     * Get pos
     *
     * @return float 
     */
    public function getPos()
    {
        return $this->pos;
    }

